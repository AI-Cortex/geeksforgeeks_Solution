// Name: Sort an Array of 1 to n
// Link: https://www.geeksforgeeks.org/problems/sort-an-array-which-contains-1-to-n-values-using-mathematical-formula/1

class Solution
{
public:
    vector<int> sortArray(vector<int> &arr)
    {
        sort(arr.begin(), arr.end());
        return arr;
    }
};
